#!/usr/bin/env bash
#
# Coverage gate.
#
# Reads coverage.out (regenerate first via `scripts/coverage/report.sh`) and
# enforces a per-package floor:
#
#   - pkg/cypher* and pkg/storage* : 95 %    (per AGENTS.md §6 "Core logic")
#   - everything else in-scope     : 90 %
#
# Out-of-scope packages (already stripped by the existing
# scripts/filter-generated-coverage.sh harness — apoc/, generated, gpu/,
# antlr/, etc.) never appear in coverage.out, so we do not have to redo the
# exclusion logic here.
#
# Modes:
#   gate.sh                  enforce the floor; exit non-zero on any violation
#   gate.sh --ratchet        write current per-package coverage to
#                            scripts/coverage/floors.txt; useful while the
#                            project is still climbing toward the final 90/95
#                            target. Once the floors file exists, `gate.sh`
#                            (no flag) ALSO enforces the per-package floor
#                            from that file in addition to the global 90/95.
#
# Exit codes:
#   0 — all packages meet their thresholds.
#   1 — at least one package below threshold (printed to stderr).
#   2 — coverage.out missing or unparseable.

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
cd "$REPO_ROOT"

PROFILE="${PROFILE:-coverage.out}"
FLOORS="scripts/coverage/floors.txt"
MODE="enforce"

if [ "${1:-}" = "--ratchet" ]; then
	MODE="ratchet"
	shift
fi

if [ ! -f "$PROFILE" ]; then
	echo "coverage gate: $PROFILE not found; run scripts/coverage/report.sh first" >&2
	exit 2
fi

python3 - "$PROFILE" "$FLOORS" "$MODE" <<'PY'
import sys, collections, os

prof, floors_path, mode = sys.argv[1], sys.argv[2], sys.argv[3]

totals = collections.defaultdict(lambda: [0, 0])
with open(prof) as fh:
    first = fh.readline()
    for line in fh:
        line = line.strip()
        if not line:
            continue
        try:
            location, num_stmt, count = line.rsplit(" ", 2)
            num_stmt = int(num_stmt); count = int(count)
        except ValueError:
            continue
        file_with_pos = location.split(":", 1)[0]
        pkg = file_with_pos.rsplit("/", 1)[0]
        totals[pkg][1] += num_stmt
        if count > 0:
            totals[pkg][0] += num_stmt

pkg_pct = {p: (100.0 * c / t if t else 0.0) for p, (c, t) in totals.items() if t > 0}

CORE_PREFIXES = (
    "github.com/orneryd/nornicdb/pkg/cypher",
    "github.com/orneryd/nornicdb/pkg/storage",
)

def threshold_for(pkg: str) -> float:
    if any(pkg == p or pkg.startswith(p + "/") for p in CORE_PREFIXES):
        return 95.0
    return 90.0

if mode == "ratchet":
    # Write per-package floor file: every package gets its current coverage
    # rounded down to one decimal place. This is the ratchet — coverage
    # cannot regress below this floor on subsequent runs even before the
    # final 90/95 target is reached.
    os.makedirs(os.path.dirname(floors_path) or ".", exist_ok=True)
    with open(floors_path, "w") as out:
        out.write("# Per-package coverage ratchet floors.\n")
        out.write("# Generated by scripts/coverage/gate.sh --ratchet.\n")
        out.write("# Format: <pkg> <floor-percent>\n")
        for pkg in sorted(pkg_pct):
            # Floor down to one decimal so floating-point noise never trips
            # the gate on the next run.
            floor = int(pkg_pct[pkg] * 10) / 10.0
            out.write(f"{pkg} {floor:.1f}\n")
    print(f"ratcheted floors for {len(pkg_pct)} packages → {floors_path}")
    sys.exit(0)

# Enforce mode.
floors = {}
if os.path.exists(floors_path):
    with open(floors_path) as fh:
        for line in fh:
            s = line.strip()
            if not s or s.startswith("#"):
                continue
            try:
                pkg, pct = s.split()
                floors[pkg] = float(pct)
            except ValueError:
                continue

violations = []
for pkg, pct in sorted(pkg_pct.items()):
    threshold = threshold_for(pkg)
    floor = max(floors.get(pkg, 0.0), 0.0)
    # Effective threshold: hard target, but at minimum the ratchet floor.
    effective = max(threshold, floor) if floor and floor > threshold else threshold
    # The gate only fails below the hard threshold, but the ratchet floor
    # also has to hold once recorded. We allow a tiny epsilon for rounding.
    epsilon = 0.05
    if pct + epsilon < threshold:
        violations.append((pkg, pct, threshold, "below hard threshold"))
        continue
    if floor and pct + epsilon < floor:
        violations.append((pkg, pct, floor, "below ratchet floor"))

if violations:
    print(f"coverage gate: {len(violations)} package(s) below threshold:",
          file=sys.stderr)
    for pkg, pct, want, why in violations:
        print(f"  {pkg}: {pct:.3f}% < {want:.1f}% ({why})", file=sys.stderr)
    sys.exit(1)

print(f"coverage gate: OK ({len(pkg_pct)} packages, all ≥ threshold)")
PY
