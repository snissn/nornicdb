#!/usr/bin/env bash
#
# Coverage reporting wrapper.
#
# Drives the existing `scripts/generate-coverage.sh` harness (which already
# applies the standard NornicDB exclusion list — apoc/, generated, gpu/,
# antlr/, etc.) and produces:
#
#   - coverage.raw.out       : merged raw per-package profile (input to filter)
#   - coverage.out           : filtered profile (the on-disk source of truth)
#   - plans/coverage-current.txt : human-readable per-package + aggregate
#                                  totals, sorted ascending by coverage %.
#
# When invoked with `--baseline`, writes plans/coverage-baseline.txt instead
# of coverage-current.txt. The baseline is the immutable reference point for
# tracking deltas; the current report is overwritten on every run.
#
# Usage:
#   bash scripts/coverage/report.sh                 # writes plans/coverage-current.txt
#   bash scripts/coverage/report.sh --baseline      # writes plans/coverage-baseline.txt
#
# Both modes regenerate coverage.out from scratch via the existing harness.

set -euo pipefail

REPO_ROOT="$(cd "$(dirname "$0")/../.." && pwd)"
cd "$REPO_ROOT"

OUT_NAME="coverage-current.txt"
if [ "${1:-}" = "--baseline" ]; then
	OUT_NAME="coverage-baseline.txt"
	shift
fi

RAW_OUT="${RAW_OUT:-coverage.raw.out}"
OUT="${OUT:-coverage.out}"

echo "→ Generating coverage profile via scripts/generate-coverage.sh ..."
echo "  raw output: $RAW_OUT"
echo "  filtered  : $OUT"
bash scripts/generate-coverage.sh "$RAW_OUT" "$OUT"

mkdir -p plans
REPORT="plans/$OUT_NAME"

echo "→ Building per-package report at $REPORT ..."

# `go tool cover -func` emits lines like:
#   github.com/orneryd/nornicdb/pkg/foo/bar.go:42: FuncName    87.5%
# plus a final `total:` row.
#
# We group by package (everything up to the last `/` in the file path) and
# compute coverage as covered/total statements. We do this via a second pass
# over the raw profile (coverage.out) so the totals match the gate exactly.

python3 - "$OUT" "$REPORT" <<'PY'
import sys, collections, os

prof_path, report_path = sys.argv[1], sys.argv[2]

# Parse the filtered profile. Format (mode: set):
#   <file>:<startLine>.<startCol>,<endLine>.<endCol> <numStmt> <count>
totals = collections.defaultdict(lambda: [0, 0])  # pkg -> [covered_stmts, total_stmts]
files_seen = set()
with open(prof_path) as fh:
    first = fh.readline()  # mode line
    for line in fh:
        line = line.strip()
        if not line:
            continue
        try:
            location, num_stmt, count = line.rsplit(" ", 2)
            num_stmt = int(num_stmt)
            count = int(count)
        except ValueError:
            continue
        file_with_pos = location.split(":", 1)[0]
        files_seen.add(file_with_pos)
        pkg = file_with_pos.rsplit("/", 1)[0]
        totals[pkg][1] += num_stmt
        if count > 0:
            totals[pkg][0] += num_stmt

rows = []
grand_cov = 0
grand_tot = 0
for pkg, (cov, tot) in totals.items():
    if tot == 0:
        continue
    pct = 100.0 * cov / tot
    rows.append((pct, pkg, cov, tot))
    grand_cov += cov
    grand_tot += tot

rows.sort(key=lambda r: (r[0], r[1]))

with open(report_path, "w") as out:
    out.write("# NornicDB coverage report\n")
    out.write("# Sorted ascending by coverage %.\n")
    out.write("# Generated by scripts/coverage/report.sh\n")
    out.write("#\n")
    out.write(f"# files counted: {len(files_seen)}\n")
    out.write(f"# packages    : {len(rows)}\n")
    if grand_tot:
        out.write(f"# aggregate   : {100.0 * grand_cov / grand_tot:7.3f}%  ({grand_cov}/{grand_tot} stmts)\n")
    else:
        out.write("# aggregate   : (no statements counted)\n")
    out.write("#\n")
    out.write(f"# {'pct':>7}  {'covered/total':>15}  package\n")
    for pct, pkg, cov, tot in rows:
        out.write(f"  {pct:7.3f}  {cov:>7}/{tot:<7}  {pkg}\n")

print(f"wrote {report_path}: {len(rows)} packages, aggregate "
      f"{(100.0*grand_cov/grand_tot if grand_tot else 0):.3f}%")
PY

echo "→ Lowest 10 in-scope packages:"
# Skip comment / blank / header lines, then take the first 10 data rows.
# (The file is already sorted ascending by coverage; data rows start with
# whitespace followed by a digit, while comment lines start with '#'.)
grep -E '^[[:space:]]+[0-9]' "$REPORT" | head -10
echo
echo "→ Aggregate:"
grep -E '^# aggregate' "$REPORT"
echo
echo "✓ Coverage report at $REPORT"
